import crypto from "node:crypto";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(__dirname, "..");
const specPath = path.join(webRoot, "design-system", "careerpilot-apac.design-system.json");
const outputDir = path.join(webRoot, "design-system", "generated");
const outputPath = path.join(outputDir, "careerpilot-figma-library.use-figma.js");
const manifestPath = path.join(outputDir, "careerpilot-figma-library.manifest.json");

function sha256(value) {
  return crypto.createHash("sha256").update(value).digest("hex");
}

function buildUseFigmaScript(spec, specHash) {
  return `// Generated by apps/web/scripts/export-figma-library.mjs
// careerpilot-design-system-hash=${specHash}
// Intended for mcp__codex_apps__figma._use_figma with skillNames: "figma-use,figma-generate-library"

const DESIGN_SYSTEM = ${JSON.stringify(spec, null, 2)};
const RUN_ID = "careerpilot-apac-ds-" + Date.now();
const createdNodeIds = [];
const mutatedNodeIds = [];

function remember(node, phase, key) {
  node.setSharedPluginData("careerpilot", "run_id", RUN_ID);
  node.setSharedPluginData("careerpilot", "phase", phase);
  node.setSharedPluginData("careerpilot", "key", key);
  mutatedNodeIds.push(node.id);
  return node;
}

function hexToRgb(hex) {
  const clean = String(hex).replace("#", "");
  const value = Number.parseInt(clean, 16);
  return {
    r: ((value >> 16) & 255) / 255,
    g: ((value >> 8) & 255) / 255,
    b: (value & 255) / 255
  };
}

function parsePixels(value) {
  return Number.parseFloat(String(value).replace("px", ""));
}

function variableScopeForToken(group, token) {
  if (group === "colors") {
    if (token.name.startsWith("text/")) return ["TEXT_FILL"];
    if (token.name.startsWith("border/")) return ["STROKE_COLOR"];
    return ["FRAME_FILL", "SHAPE_FILL"];
  }
  if (group === "spacing") return ["GAP"];
  if (group === "radii") return ["CORNER_RADIUS"];
  return [];
}

function variableTypeForGroup(group) {
  return group === "colors" ? "COLOR" : "FLOAT";
}

function valueForToken(group, token) {
  return group === "colors" ? hexToRgb(token.value) : parsePixels(token.value);
}

async function getOrCreateCollection(name) {
  const existing = (await figma.variables.getLocalVariableCollectionsAsync()).find((collection) => collection.name === name);
  if (existing) return existing;

  const collection = figma.variables.createVariableCollection(name);
  if (collection.modes?.[0]) {
    collection.renameMode(collection.modes[0].modeId, "Value");
  }
  return collection;
}

async function getVariableInCollection(collection, name) {
  const variables = await Promise.all(collection.variableIds.map((id) => figma.variables.getVariableByIdAsync(id)));
  return variables.find((variable) => variable?.name === name) || null;
}

async function createTokens() {
  const collections = {};
  const variables = {};

  for (const [group, items] of Object.entries(DESIGN_SYSTEM.tokens)) {
    if (group === "motion" || group === "shadows") continue;

    const collection = await getOrCreateCollection("CareerPilot / " + group);
    const modeId = collection.modes[0].modeId;
    collections[group] = collection.id;

    for (const token of items) {
      const name = token.figmaName.replaceAll("/", " / ");
      const type = variableTypeForGroup(group);
      const existing = await getVariableInCollection(collection, name);
      const variable = existing || figma.variables.createVariable(name, collection, type);

      variable.setValueForMode(modeId, valueForToken(group, token));
      variable.scopes = variableScopeForToken(group, token);
      if (token.css) {
        variable.setVariableCodeSyntax("WEB", "var(" + token.css + ")");
      }

      variables[token.figmaName] = variable.id;
      if (!existing) createdNodeIds.push(variable.id);
    }
  }

  return { collections, variables };
}

async function createTextStyles() {
  await figma.loadFontAsync({ family: "Inter", style: "Regular" });
  const localStyles = figma.getLocalTextStyles();
  const styles = {};

  for (const item of DESIGN_SYSTEM.typography) {
    let style = localStyles.find((candidate) => candidate.name === "CareerPilot / " + item.name);
    if (!style) {
      style = figma.createTextStyle();
      style.name = "CareerPilot / " + item.name;
    }

    const size = parsePixels(item.size);
    style.fontName = { family: "Inter", style: "Regular" };
    style.fontSize = size;
    style.lineHeight = item.lineHeight === "1" ? { unit: "PIXELS", value: size } : { unit: "PIXELS", value: Math.round(size * Number.parseFloat(item.lineHeight)) };
    style.letterSpacing = { unit: "PIXELS", value: 0 };
    style.description = "CareerPilot APAC " + item.name;
    styles[item.name] = style.id;
  }

  return styles;
}

function shadowEffectFromToken(token) {
  const match = token.value.match(/0\\s+([\\d.]+)px\\s+([\\d.]+)px\\s+(-?[\\d.]+)px\\s+rgb\\((\\d+)\\s+(\\d+)\\s+(\\d+)\\s+\\/\\s+([\\d.]+)\\)/);
  if (!match) return [];
  return [
    {
      type: "DROP_SHADOW",
      visible: true,
      blendMode: "NORMAL",
      color: {
        r: Number(match[5]) / 255,
        g: Number(match[6]) / 255,
        b: Number(match[7]) / 255,
        a: Number(match[8])
      },
      offset: { x: 0, y: Number(match[1]) },
      radius: Number(match[2]),
      spread: Number(match[3])
    }
  ];
}

function createEffectStyles() {
  const localStyles = figma.getLocalEffectStyles();
  const styles = {};

  for (const token of DESIGN_SYSTEM.tokens.shadows) {
    let style = localStyles.find((candidate) => candidate.name === "CareerPilot / " + token.figmaName);
    if (!style) {
      style = figma.createEffectStyle();
      style.name = "CareerPilot / " + token.figmaName;
    }
    style.effects = shadowEffectFromToken(token);
    style.description = token.css + " = " + token.value;
    styles[token.figmaName] = style.id;
  }

  return styles;
}

async function getOrCreatePage(name) {
  let page = figma.root.children.find((candidate) => candidate.name === name);
  if (!page) {
    page = figma.createPage();
    page.name = name;
    createdNodeIds.push(page.id);
  }
  remember(page, "page", name);
  return page;
}

async function addText(parent, text, size, color, width) {
  const node = figma.createText();
  await figma.loadFontAsync({ family: "Inter", style: "Regular" });
  node.fontName = { family: "Inter", style: "Regular" };
  node.characters = text;
  node.fontSize = size;
  node.lineHeight = { unit: "PIXELS", value: Math.round(size * 1.35) };
  node.fills = [{ type: "SOLID", color }];
  node.textAutoResize = "HEIGHT";
  node.resize(width, node.height);
  parent.appendChild(node);
  createdNodeIds.push(node.id);
  return node;
}

async function createSectionFrame(page, name, x, y, width) {
  const frame = figma.createAutoLayout("VERTICAL", {
    name,
    itemSpacing: 16,
    paddingTop: 24,
    paddingRight: 24,
    paddingBottom: 24,
    paddingLeft: 24
  });
  frame.resize(width, 10);
  frame.x = x;
  frame.y = y;
  frame.fills = [{ type: "SOLID", color: hexToRgb("#ffffff") }];
  frame.strokes = [{ type: "SOLID", color: hexToRgb("#d8dee8") }];
  frame.cornerRadius = 16;
  page.appendChild(frame);
  createdNodeIds.push(frame.id);
  remember(frame, "documentation", name);
  return frame;
}

async function buildDocumentationPages() {
  const cover = await getOrCreatePage("Cover");
  await figma.setCurrentPageAsync(cover);
  const coverFrame = await createSectionFrame(cover, "CareerPilot APAC / Cover", 80, 80, 760);
  await addText(coverFrame, "CareerPilot APAC Design System", 34, hexToRgb("#101722"), 680);
  await addText(coverFrame, "Local-first job search operating system UI library. Source of truth: code tokens, Chinese UI, safe automation boundaries.", 15, hexToRgb("#647083"), 680);

  const foundations = await getOrCreatePage("Foundations / Tokens");
  await figma.setCurrentPageAsync(foundations);
  const colorFrame = await createSectionFrame(foundations, "Color Tokens", 80, 80, 880);
  await addText(colorFrame, "Color Tokens", 26, hexToRgb("#101722"), 780);
  for (const token of DESIGN_SYSTEM.tokens.colors) {
    await addText(colorFrame, token.figmaName + "  " + token.css + "  " + token.value, 13, hexToRgb("#2f3a4a"), 780);
  }

  const scaleFrame = await createSectionFrame(foundations, "Spacing / Radius", 1000, 80, 520);
  await addText(scaleFrame, "Spacing + Radius", 26, hexToRgb("#101722"), 440);
  for (const token of [...DESIGN_SYSTEM.tokens.spacing, ...DESIGN_SYSTEM.tokens.radii]) {
    await addText(scaleFrame, token.figmaName + "  " + token.value, 13, hexToRgb("#2f3a4a"), 440);
  }
}

async function buildComponentPages() {
  const page = await getOrCreatePage("Components");
  await figma.setCurrentPageAsync(page);

  let x = 80;
  let y = 80;
  for (const component of DESIGN_SYSTEM.components) {
    const card = await createSectionFrame(page, component.figmaComponent, x, y, 360);
    await addText(card, component.figmaComponent, 20, hexToRgb("#101722"), 300);
    await addText(card, component.usage, 13, hexToRgb("#647083"), 300);
    await addText(card, "States: " + component.states.join(", "), 12, hexToRgb("#087869"), 300);
    await addText(card, "Classes: " + component.classes.map((item) => "." + item).join("  "), 11, hexToRgb("#647083"), 300);

    const componentNode = figma.createComponent();
    componentNode.name = component.figmaComponent;
    componentNode.resize(320, 88);
    componentNode.fills = [{ type: "SOLID", color: hexToRgb("#f8fafc") }];
    componentNode.strokes = [{ type: "SOLID", color: hexToRgb("#d8dee8") }];
    componentNode.cornerRadius = 12;
    componentNode.setSharedPluginData("careerpilot", "key", component.name);
    card.appendChild(componentNode);
    createdNodeIds.push(componentNode.id);

    x += 420;
    if (x > 1340) {
      x = 80;
      y += 430;
    }
  }
}

async function main() {
  const tokenSummary = await createTokens();
  const textStyles = await createTextStyles();
  const effectStyles = createEffectStyles();
  await buildDocumentationPages();
  await buildComponentPages();

  return {
    ok: true,
    runId: RUN_ID,
    product: DESIGN_SYSTEM.product,
    version: DESIGN_SYSTEM.version,
    tokenCount: Object.values(DESIGN_SYSTEM.tokens).flat().length,
    componentCount: DESIGN_SYSTEM.components.length,
    tokenSummary,
    textStyleCount: Object.keys(textStyles).length,
    effectStyleCount: Object.keys(effectStyles).length,
    createdNodeIds,
    mutatedNodeIds
  };
}

return await main();
`;
}

async function main() {
  const specRaw = await fs.readFile(specPath, "utf8");
  const spec = JSON.parse(specRaw);
  const specHash = sha256(specRaw);
  const script = buildUseFigmaScript(spec, specHash);
  const manifest = {
    ok: true,
    generatedAt: new Date().toISOString(),
    specHash,
    output: path.relative(webRoot, outputPath).replaceAll("\\\\", "/"),
    tokenCount: Object.values(spec.tokens).flat().length,
    componentCount: spec.components.length,
    figmaComponents: spec.components.map((component) => component.figmaComponent)
  };

  await fs.mkdir(outputDir, { recursive: true });
  await fs.writeFile(outputPath, script, "utf8");
  await fs.writeFile(manifestPath, JSON.stringify(manifest, null, 2), "utf8");
  console.log(JSON.stringify(manifest));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
